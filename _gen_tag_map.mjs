async function main() {
  await client.connect();
  const db = client.db();
  const levels = await db.collection('courselevels').find({}).toArray();

  // 构建 chapterId → 是否存在 的映射（验证用）
  const validIds = new Set();
  for (const lv of levels) {
    for (const topic of (lv.topics || [])) {
      for (const ch of (topic.chapters || [])) {
        if (ch.id) validIds.add(ch.id);
      }
    }
  }

  // 验证 MANUAL_MAP 中的 ID 是否真实存在
  let missing = 0;
  for (const [tag, id] of Object.entries(MANUAL_MAP)) {
    if (!validIds.has(id)) {
      console.warn(`⚠️  [${tag}] → "${id}" 不在DB中`);
      missing++;
    }
  }

  const outPath = path.join(process.cwd(), 'src/utils/gespTagMap.json');
  fs.writeFileSync(outPath, JSON.stringify(MANUAL_MAP, null, 2), 'utf-8');

  console.log(`\n✅ 写出 src/utils/gespTagMap.json`);
  console.log(`   共 ${Object.keys(MANUAL_MAP).length} 个标签映射`);
  if (missing) console.log(`   ⚠️  ${missing} 个 chapterId 在DB中未找到，请手动修正`);
  else console.log(`   全部 chapterId 验证通过 ✓`);
}

main().catch(console.error).finally(() => client.close());
