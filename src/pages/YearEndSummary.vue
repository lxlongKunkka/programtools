import { reactive, ref, nextTick, onMounted } from 'vue'
import axios from 'axios'
import MarkdownViewer from '../components/MarkdownViewer.vue'
import { getModels } from '../utils/models'

export default {
  name: 'YearEndSummary',
  components: {
    MarkdownViewer
  },
  setup() {
    const form = reactive({
      role: '',
      keywords: '',
      achievements: '',
      challenges: '',
      plans: '',
      style: 'æ­£å¼ä¸¥è°¨',
      length: '800å­—',
      model: 'o4-mini',
      temperature: 0.7
    })

    const generatedSummary = ref('')
    const copied = ref(false)
    const loading = ref(false)
    const showAdvanced = ref(false)
    const modelOptions = ref([])

    onMounted(async () => {
      try {
        const list = await getModels()
        if (Array.isArray(list)) {
          modelOptions.value = list
          // é»˜è®¤é€‰ä¸­ç¬¬ä¸€ä¸ªæˆ–è€… o4-mini
          if (!form.model && list.length > 0) form.model = list[0].id
        }
      } catch (e) { console.warn('failed to load models', e) }
    })

    const generateSummary = async () => {
      if (!form.role && !form.achievements) {
        alert('è¯·è‡³å°‘å¡«å†™å²—ä½å’Œä¸»è¦æˆå°±')
        return
      }

      loading.value = true
      generatedSummary.value = '' 
      
      // å¹³æ»‘æ»šåŠ¨åˆ°ç»“æœåŒºåŸŸ
      nextTick(() => {
        const resultArea = document.getElementById('result-area')
        if (resultArea) {
          resultArea.scrollIntoView({ behavior: 'smooth', block: 'start' })
        }
      })

      try {
        const response = await axios.post('/api/summary', form)
        if (response.data && response.data.result) {
          generatedSummary.value = response.data.result
        } else {
          generatedSummary.value = '> ç”Ÿæˆå¤±è´¥ï¼šæœåŠ¡å™¨æœªè¿”å›æœ‰æ•ˆå†…å®¹ã€‚'
        }
      } catch (error) {
        console.error('Error:', error)
        generatedSummary.value = `> ç”Ÿæˆå¤±è´¥: ${error.response?.data?.error || error.message}`
      } finally {
        loading.value = false
      }
    }

    const copyToClipboard = async () => {
      try {
        await navigator.clipboard.writeText(generatedSummary.value)
        copied.value = true
        setTimeout(() => {
          copied.value = false
        }, 2000)
      } catch (err) {
        alert('å¤åˆ¶å¤±è´¥ï¼Œè¯·æ‰‹åŠ¨å¤åˆ¶')
      }
    }

    const downloadSummary = () => {
      if (!generatedSummary.value) return
      
      const dateStr = new Date().toISOString().split('T')[0]
      const roleStr = form.role ? `_${form.role}` : ''
      const filename = `å¹´ç»ˆæ€»ç»“${roleStr}_${dateStr}.md`
      
      const blob = new Blob([generatedSummary.value], { type: 'text/markdown;charset=utf-8;' })
      const link = document.createElement('a')
      if (link.download !== undefined) {
        const url = URL.createObjectURL(blob)
        link.setAttribute('href', url)
        link.setAttribute('download', filename)
        link.style.visibility = 'hidden'
        document.body.appendChild(link)
        link.click()
        document.body.removeChild(link)
      }
    }

    return {
      form,
      generatedSummary,
      loading,
      copied,
      showAdvanced,
      modelOptions,
      generateSummary,
      copyToClipboard,
      downloadSummary
    }
  }
}
</script>

